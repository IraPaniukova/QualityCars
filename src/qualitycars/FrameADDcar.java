    private void enterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonActionPerformed
        String model = modelTextField.getText();
        manufactureTextField.setEditable(true);
        engineSizeTextField.setEditable(true);
        seatsTextField.setEditable(true);
        fuelTextField.setEditable(true);
        
        CarModel found = FrameHOME.car.searchModel(model);
        if (found==null)
        {   
            if(modelTextField.getText().equals(("")))
            {JOptionPane.showMessageDialog(rootPane, "Please enter the name of the model.");}
            else{
            JOptionPane.showMessageDialog(rootPane, "This model doesn't exist. Please add it.");
            FrameADDmodel stock = new FrameADDmodel();
            stock.my_updateAddModel(model);
            stock.setVisible(true);
            }}
            
        else{
            manufactureTextField.setText(found.getManufacturer());
            engineSizeTextField.setText(found.getEngineSize()+"");
            seatsTextField.setText(found.getSeats()+"");
            fuelTextField.setText(found.getFuelType()+"");
            
            manufactureTextField.setEditable(false);
            engineSizeTextField.setEditable(false);
            seatsTextField.setEditable(false);
            fuelTextField.setEditable(false);
            this.Panel.setVisible(true);
            
            }
           
            
            
    }//GEN-LAST:event_enterButtonActionPerformed

