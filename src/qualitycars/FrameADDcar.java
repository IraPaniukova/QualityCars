    private void addButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton3ActionPerformed
        // TODO add your handling code here:
        String model = modelTextField.getText();  //I'm adding this code block just in case a user will add a random model one more time
        CarModel foundM = FrameHOME.car.searchModel(model);
        if (foundM==null)
        {   
            if(modelTextField.getText().equals(("")))
            {JOptionPane.showMessageDialog(rootPane, "Please enter the name of the model.");}
            else{
            JOptionPane.showMessageDialog(rootPane, "This model doesn't exist. Please add it.");
            FrameADDmodel stock = new FrameADDmodel();
            stock.my_updateAddModel(model);
            stock.setVisible(true);
            }}
        else{
            String regNumber = regNumberTextField.getText();
            IndividualCar foundCar = FrameHOME.stock.searchCarByRegNumber(regNumber); 
            CarModel found = FrameHOME.car.searchModel(modelTextField.getText());
                if (foundCar!=null)
                {   regNumberTextField.setBackground(Color.red);
                    JOptionPane.showMessageDialog(rootPane, "The car with this number already exists");}
                else{
                    try{    
                        regNumberTextField.setBackground(Color.white);
                        CarModel carModel = new CarModel(found.getModel(),found.getManufacturer(),
                                                        found.getEngineSize(),found.getSeats(), found.getFuelType());
                        if(regNumber.equals("")||expDateWOFDatePicker.getDate()==null||yearTextField.getText().equals("")||
                                                colourTextField.getText().equals("")||rentPerDaytField.getText().equals(""))
                        {JOptionPane.showMessageDialog(rootPane, "Some fields are empty");}
                        else{
                            try{ 
                                IndividualCar car = new IndividualCar(carModel,regNumber,expDateWOFDatePicker.getDate(),
                                                (EnumBodyType) bodyTypeComboBox.getSelectedItem(),Integer.parseInt(yearTextField.getText()),
                                                 colourTextField.getText(),Double.parseDouble(rentPerDaytField.getText()),
                                                EnumStatus.IN_THE_SHOP);
                                FrameHOME.stock.addCar(car);
                                JOptionPane.showMessageDialog(rootPane, "The car is added.");
                                regNumberTextField.setText("");
                                expDateWOFDatePicker.setDate(null);
                                yearTextField.setText("");
                                colourTextField.setText("");
                                rentPerDaytField.setText("");
                                this.Panel.setVisible(false);
                                modelTextField.setText("");
                                manufactureTextField.setText("");
                                engineSizeTextField.setText("");
                                seatsTextField.setText("");
                                fuelTextField.setText("");}
                            catch(Exception ex)
                            {    JOptionPane.showMessageDialog(rootPane, 
                                  "1. Check YEAR OF MANUFACTURE. It cannot be negative and can contain only digits."
                              + "\n   Cars didn't exist before 1886. A car couldn't be produced later than current year."
                             + "\n\n             2. Check RENT PER DAY. It cannot be negative and can contain only digits.\n ");}
                            }}
                    catch (Exception ex){ex.printStackTrace();}
                    }
        }
  
    }//GEN-LAST:event_addButton3ActionPerformed

       
