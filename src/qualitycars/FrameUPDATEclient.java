    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(idTextField.getText());
        found.setPersonName(nameTextField.getText());
        found.setBirthDate(birthDatePicker.getDate());
        found.setGender((EnumGender) genderComboBox.getSelectedItem());
        found.setPhone(phoneTextField.getText());
        found.setAddress(addressTextField.getText());
        found.setLicenseNumber(licenseNumberField.getText());
        found.setLicenseType(licenseTypeField.getText());
        found.setLicenseExpDate(licenseExpDatePicker.getDate());
                 
         RentedCar foundRC= FrameHOME.stock.searchRentedCarByClientID(id); 
          if (FrameHOME.stock.getRentedCars().isEmpty()==false && foundRC.getClient().getPersonId()==id) 
          { foundRC.setClient(found);} 
        JOptionPane.showMessageDialog(rootPane, "The client information is updated.");
        idTextField.setEditable(true);
        jPanel1.setVisible(false);
        jPanel2.setVisible(false);
        
    }//GEN-LAST:event_updateButtonActionPerformed

