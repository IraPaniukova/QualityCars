    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt( idTextField.getText());
        found = FrameHOME.stock.searchClient(id);
        
        if (found==null)
        {idTextField.setBackground(Color.red);
         JOptionPane.showMessageDialog(rootPane, "The client with such ID is not in our database");}
        else{   
            idTextField.setBackground(Color.white);
            jPanel1.setVisible(true);
            nameTextField.setText(found.getPersonName());
            birthDatePicker.setDate(found.getBirthDate());
            genderComboBox.setSelectedItem(found.getGender());
            phoneTextField.setText(found.getPhone());
            addressTextField.setText(found.getAddress());
            licenseNumberField.setText(found.getLicenseNumber());
            licenseTypeField.setText(found.getLicenseType());
            licenseExpDatePicker.setDate(found.getLicenseExpDate());
            
            jPanel2.setVisible(true);
            DefaultTableModel table = (DefaultTableModel) modelTable.getModel();
            Object rowColumns[]=new Object[5];
            table.setRowCount(0);
            ArrayList<String> temporaryArrayList= new ArrayList();
            ArrayList<RentedCar> car = FrameHOME.stock.getRentedCars();
            
            for (int i=0; i<car.size();i++)
            {   if (car.get(i).getClient().getPersonId()==id)
                {
                rowColumns[0]= car.get(i).getCar().getRegNumber();
                rowColumns[1]= car.get(i).getCar().getColour();
                rowColumns[2]= car.get(i).getCar().getCarModel().getModel();
                rowColumns[3]= car.get(i).getCar().getRentDate();
                rowColumns[4]= car.get(i).getCar().getBackDueDate();
                table.addRow(rowColumns);
                temporaryArrayList.add(car.get(i).getCar().getRegNumber());
                } 
             } idTextField.setEditable(false);
        }

    }//GEN-LAST:event_searchButtonActionPerformed

