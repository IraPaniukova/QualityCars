    private void rentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rentButtonActionPerformed
        // TODO add your handling code here:
        String regNumber = regNumberComboBox.getSelectedItem().toString();
        IndividualCar foundCar = FrameHOME.stock.searchCarByRegNumber(regNumber); 
        
        Date rentDate = rentDatePicker.getDate();
        Date backDueDate = backDueDatePicker.getDate();
        while (rentDate==null||backDueDate==null)
        {JOptionPane.showMessageDialog(rootPane, "Set rent dates."); break;}
        if (rentDate!=null&&backDueDate!=null)
        {  
            if ( rentDate.compareTo(backDueDate)>0)
                {JOptionPane.showMessageDialog(rootPane, "Check you rent dates.");} 
            else{
                foundCar.setRentDate(rentDate);
                foundCar.setBackDueDate(backDueDate);
                jPanel2.setVisible(false);
                jPanel3.setVisible(false);
                foundCar.setStatus(EnumStatus.RENTED_OUT);
                JOptionPane.showMessageDialog(rootPane, "The car # "+regNumber+" ("+foundCar.getColour()+" "
                        +foundCar.getCarModel().getModel()+" ) was rented. "); 
                 RentedCar rentedCar = new RentedCar(found,foundCar); //or client?
                 FrameHOME.stock.addRentedCar(rentedCar); 
                 
                 Date todayIs = new Date();
                 SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMdd");
                 if(!sdf.format(todayIs).equals(sdf.format(found.getDate()))) 
                 {found.setNewOrOld("old");}
                }
        }
        

    }//GEN-LAST:event_rentButtonActionPerformed

    private void enterIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterIdButtonActionPerformed
        // TODO add your handling code here:
        if (idTextField.getText().equals(""))
        {JOptionPane.showMessageDialog(rootPane, "Please enter client ID.");}
        else{
            int id = Integer.parseInt(idTextField.getText());
            found = FrameHOME.stock.searchClient(id);
             if (found==null)
            {   idTextField.setBackground(Color.red);
                JOptionPane.showMessageDialog(rootPane, "Client with such ID is not in our database. Please add client's data.");
                FrameADDclient client = new FrameADDclient();
                client.my_updateAddClientId(id); //to pass data to FrameADDclient
                client.setVisible(true);
                idTextField.setBackground(Color.white);
                 }
             else
             {  
                 ArrayList<IndividualCar> car = FrameHOME.stock.getCars();
            int x = 0;
            for(int i=0;i<car.size();i++)
                if (car.get(i).getStatus().equals(EnumStatus.IN_THE_SHOP))
                {x++;}
            if (x==0){JOptionPane.showMessageDialog(rootPane, "There are no cars to rent.");} 
            else{

                nameTextField.setText(found.getPersonName());
                birthDatePicker.setDate(found.getBirthDate());
                genderTextField.setText(found.getGender().toString());
                phoneTextField.setText(found.getPhone());
                addressTextField.setText(found.getAddress());
                licenseNumberField.setText(found.getLicenseNumber());
                licenseTypeField.setText(found.getLicenseType());
                licenseExpDatePicker.setDate(found.getLicenseExpDate());

                String[] ArrayModels = new String [FrameHOME.car.getModelArrayList().size()];
    //            System.out.println(FrameHOME.car.getModels().size());
                for (int i=0; i<FrameHOME.car.getModelArrayList().size();i++){ArrayModels[i]=FrameHOME.car.getModelArrayList().get(i).getModel();}
    //            System.out.println(Arrays.toString(ArrayModels));
                modelComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(ArrayModels));

                 jPanel1.setVisible(true);
             }}
        }
    }//GEN-LAST:event_enterIdButtonActionPerformed

