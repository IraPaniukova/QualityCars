    private void enterModelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterModelButtonActionPerformed
        // TODO add your handling code here:
        
        if (FrameHOME.stock.noCarsForRent())
           {JOptionPane.showMessageDialog(rootPane, "There are no cars for rent");}
        else {
            String model = modelComboBox.getSelectedItem().toString();
            CarModel foundM = FrameHOME.car.searchModel(model);
            manufacturertTextField.setText(foundM.getManufacturer());
            engineSizeTextField.setText(foundM.getEngineSize()+"");
            seatsTextField.setText(foundM.getSeats()+"");
            fuelTextField.setText(foundM.getFuelType()+"");
            DefaultTableModel table = (DefaultTableModel) modelTable.getModel();
            Object rowColumns[]=new Object[6];
            table.setRowCount(0);  
            ArrayList<String> temporaryArrayList= new ArrayList();
            for(int i=0;i<FrameHOME.stock.getCars().size();i++)
            {
            if (FrameHOME.stock.getCars().get(i).getStatus().equals(EnumStatus.IN_THE_SHOP)&&FrameHOME.stock.getCars().get(i).getCarModel().getModel().equals(model))                                   
                {   
                rowColumns[0]= FrameHOME.stock.getCars().get(i).getRegNumber();
                rowColumns[1]= FrameHOME.stock.getCars().get(i).getExpDateWOF();
                rowColumns[2]= FrameHOME.stock.getCars().get(i).getBodyType();
                rowColumns[3]= FrameHOME.stock.getCars().get(i).getYear();
                rowColumns[4]= FrameHOME.stock.getCars().get(i).getColour();
                rowColumns[5]= FrameHOME.stock.getCars().get(i).getRentPerDay();
                table.addRow(rowColumns);
                temporaryArrayList.add(FrameHOME.stock.getCars().get(i).getRegNumber());
                }
            }
            String[] ArrayRegNumbers = new String[temporaryArrayList.size()]; 
            ArrayRegNumbers = temporaryArrayList.toArray(ArrayRegNumbers);    
            regNumberComboBox.setModel(new javax.swing.DefaultComboBoxModel<>(ArrayRegNumbers));

            if (temporaryArrayList.isEmpty())
            {jPanel3.setVisible(false);
                JOptionPane.showMessageDialog(rootPane,"This model doesn't have cars for rent, please choose another available model");}
            else{jPanel2.setVisible(true);
            jPanel3.setVisible(true);}
        }
    }//GEN-LAST:event_enterModelButtonActionPerformed


    
